<div class="justif textnorm">
    Toute l'équipe de l'Hopital Condorcet de Mons est fière de vous présenter son extension sur internet. Après des années de recherches pour trouver comment améliorer l'accès et prise de rendez-vous à l'hôpital, nous avons fini par opter pour un site ouvert à tous et multiplateforme.
    <br/>
    <br/>
    « Nous avions eu régulièrement des demandes quant à l'instoraitons d'un site pour notre implantation, nous avons dû faire 
    appel à la région pour trouver les fonds nécessaires et après un appel à candidatures, nous avons trouvé un partenaire
    avec qui travailler. Quelques mois plus tard, les premiers tests étaient déjà positifs. Nous espérons plaire à tout le monde 
    car même avec ce nouveau moyen, nos secrétaires seront toujours là pour vous conseiller et vous aider sur place ou au téléphone. »<br/>
    <span class="italic"> J. Vanderker, Directeur de l'HC.</span>

