<div class="justif textnorm">
    Toute l'équipe de l'Hopital Condorcet de Mons est fière de vous présenter son etension sur internet. Après des années de recherches pour trouver comment améliorer l'accès et prise de rendez-vous à l'hopital, nous avons finit par opter pour un site ouvert à tous et multiplatforme.
    <br/>
    <br/>
     « Nous avions eu régulièrement des demande quant à l'instoraitons d'un site pour notre implantation, nous avons dû faire 
     appel à la région pour trouver les fonds nécessaire et après un appel à candidatures, nous avons trouvé un partenaire
     avec qui travailler. Quelques mois plus tard les premiers test étaient déjà positif. Nous esperons esperons plaire à tout le monde 
     car même avec ce nouveau moyens, nos secrétaires seront toujours là pour vous conseiller et vous aider sur place ou au téléphone. »
     <span class="italic"> J. Vanderker, Directeur de l'HC.</span>
    
